/* ---------- MODAL ---------- */

const modal = document.getElementById("Modal"); // Get the modal
const closeBtn = document.getElementById("CloseBtn"); // Get the <span> element that closes the modal

// Modal öffnen
function openModal(event) {
  modal.style.display = "flex";
  const projectKey = event.currentTarget.getAttribute("data-project");
  const project = projects[projectKey];

  // Modal-Content aktualisieren
  document.querySelector(".modal-title").textContent = project.name;
  document.querySelector(".modal-date").textContent = project.date;
  document.querySelector(".modal-info").textContent = project.info;
  document.querySelector(".modal-technologies").textContent =
    project.technologies;

  // Überprüfen, ob Projekt Link hat
  if (project.hasOwnProperty("link")) {
    document.querySelector(".modal-link").href = project.link;
    document.querySelector(".modal-link").style.display = "flex"; // show link
  } else {
    document.querySelector(".modal-link").style.display = "none"; // hide link
  }

  // Bildergalerie
  const imageContainer = document.querySelector(".modal-images");
  imageContainer.innerHTML = ""; // Container leeren

  // Bilder zu Modal hinzufügen
  project.images.forEach((imageUrl) => {
    const image = document.createElement("img");
    image.src = imageUrl;
    imageContainer.appendChild(image);
  });
}

// Modal schließen, wenn "x" geklickt wird
closeBtn.onclick = function () {
  modal.style.display = "none";
};

// Modal schließen, wenn auf Fläche außerhalb geklickt wird
window.onclick = function (event) {
  if (event.target == modal) {
    modal.style.display = "none";
  }
};

 // ACCESSIBILITY - Klick simulieren 
function simulateClick(event) {
  // Modal öffnen, wenn Enter gedrückt wird
  if (event.key === "Enter") {
    event.preventDefault();
    openModal(event);
  }
  // Modal schließen, wenn Escape gedrückt wird
  if (event.key === "Escape") {
    event.preventDefault();
    modal.style.display = "none";
  }
};